DIAGNOSI ; ICD Diagnosis Pointer
 S:'$L($G(PL("DIAGNOSIS"))) PL("DIAGNOSIS")=$$NOS^GMPLEXT()
 Q:$$ICDCODE^GMPLEXT(+PL("DIAGNOSIS"))=""
 S GMPQUIT=1,PLY(0)=$$EZBLD^DIALOG(1250000.461)
 Q
 ;
