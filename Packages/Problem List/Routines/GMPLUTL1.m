 S %DT="P",%DT(0)="-NOW",X=PL("RECORDED") D ^%DT
 I Y'>0 S GMPQUIT=1,PLY(0)=$$EZBLD^DIALOG(1250000.472) Q
 I PL("RECORDED")<PL("ONSET") S GMPQUIT=1,PLY(0)=$$EZBLD^DIALOG(1250000.460) Q
 S PL("RECORDED")=Y
 Q
