 N DUPL,GMPARAM
 D GET^GMPLSITE(.GMPARAM)
 Q:GMPARAM("SDP")'=1
 S:'$L($G(PL("DIAGNOSIS"))) PL("DIAGNOSIS")=$$NOS^GMPLEXT()
 S %=$$DUPL^GMPLAPI2(.DUPL,GMPDFN,+PL("DIAGNOSIS"),PL("NARRATIVE"),1)
