 S %DT="P",%DT(0)="-NOW",X=PL("ONSET") D ^%DT
 I Y>0 S PL("ONSET")=Y Q
 S GMPQUIT=1,PLY(0)=$$EZBLD^DIALOG(1250000.468)
 Q
 ;
RESOLVED ; Date Resolved (Requires STATUS, ONSET)
